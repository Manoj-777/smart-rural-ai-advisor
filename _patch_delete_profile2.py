file_path = "frontend/src/i18n/translations.js"

with open(file_path, "r", encoding="utf-8") as f:
    content = f.read()

total_added = 0

for key, langs in KEYS.items():
    for lang_code, text in langs.items():
        # Find the profileTip line in this language block
        # The language block starts with 'lang_code': { and profileTip is indented with 8 spaces
        escaped = text.replace("'", "\\'")
        new_line = f"        {key}: '{escaped}',"
        
        # Find the anchor: profileTip in this lang block
        # We need to find profileTip that's inside the right language block
        # Strategy: search for the lang block header then find the first profileTip after it
        
        lang_header = f"    '{lang_code}': {{"
        header_pos = content.find(lang_header)
        if header_pos == -1:
            print(f"  WARNING: Language block '{lang_code}' not found!")
            continue
        
        # Find profileTip after this position
        profile_tip_search = "        profileTip:"
        tip_pos = content.find(profile_tip_search, header_pos)
        if tip_pos == -1:
            print(f"  WARNING: profileTip not found in '{lang_code}' block!")
            continue
        
        # Check if key already exists in this lang block
        check_str = f"        {key}:"
        # Check between header_pos and tip_pos
        existing = content.find(check_str, header_pos, tip_pos + 500)
        if existing != -1 and existing < tip_pos + 200:
            print(f"  SKIP: {key} already exists in {lang_code}")
            continue
        
        # Insert the new line before profileTip
        content = content[:tip_pos] + new_line + "\n" + content[tip_pos:]
        total_added += 1

with open(file_path, "w", encoding="utf-8") as f:
    f.write(content)

print(f"Done! Added {total_added} translation entries.")
